// Inteiros
let num = 1;
console.log(num + ' ' + typeof num)

// Float
const float = 0.03;
console.log(float + ' ' + typeof float)

// Booleano
let flag = true | false;

// Texto (String)
const text = '10';
console.log(text + ' ' + typeof text)

// Array
const array = [1, 2, 3];
console.log(array + ' ' + typeof array)

// Objeto
const obj = {
  nome: 'Carlos'
}
console.log(JSON.stringify(obj) + ' ' + typeof obj)

// BigInt
const bigint = 10n;
console.log(bigint + ' ' + typeof bigint)
